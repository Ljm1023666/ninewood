interface EmptyStateProps {
  type?: string
  message?: string
  actionLabel?: string
  variant?: 'default' | 'error'
  onAction?: () => void
  /** 自定义主操作区（提供时不再渲染默认 motion.button，由调用方自行处理点击） */
  actionSlot?: ReactNode
  className?: string
}

export function EmptyState({
  type = 'demand',
  message,
  actionLabel,
  variant = 'default',
  onAction,
  actionSlot,
  className,
}: EmptyStateProps) {
  const defaults: Record<
    string,
    { title: string; message: string; action?: string }
  > = {
    demand: {
      title: '暂无需求',
      message: '还没有任何需求发布，快来发布第一个吧',
      action: '发布需求',
    },
    message: {
      title: '暂无消息',
      message: '还没有任何聊天消息，快去找人聊聊吧',
      action: '去发现',
    },
    order: {
      title: '暂无订单',
      message: '没有任何订单记录',
      action: undefined,
    },
    circle: {
      title: '暂无圈子',
      message: '还没有加入任何圈子，去发现有趣的圈子吧',
      action: '发现圈子',
    },
    search: {
      title: '暂无结果',
      message: '没有找到匹配的内容，试试其他关键词',
      action: undefined,
    },
    video: {
      title: '暂无视频',
      message: '还没有短视频内容，稍后再来看看',
      action: '返回首页',
    },
  }

  const d = defaults[type] || defaults.demand
  const title = d.title
  const desc = message || d.message
  const icons = iconMap[type] || iconMap.demand
  const isError = variant === 'error'
  const isDark = useThemeStore((s) => s.current.dark)

  return (
    <div
      className={cn(
        'flex flex-col items-center justify-center py-16 px-6 text-center',
        className,
      )}
    >
      <motion.div
        className="flex justify-center isolate mb-6"
        initial="initial"
        animate="animate"
      >
        {[ICON_VARIANTS.left, ICON_VARIANTS.center, ICON_VARIANTS.right].map(
          (variant, i) => (
            <motion.div
              key={i}
              variants={variant}
              className={cn(
                'w-11 h-11 rounded-xl flex items-center justify-center border transition-colors duration-300',
                isError
                  ? 'bg-red-500/10 border-red-500/20 text-red-400'
                  : cn(
                      isDark
                        ? 'bg-white/[0.06] border-white/10 text-neutral-400'
                        : 'bg-black/[0.04] border-black/[0.08] text-text-muted',
                    ),
              )}
            >
              {icons[i]}
            </motion.div>
          ),
        )}
      </motion.div>

      <motion.h3
        initial={{ y: 10, opacity: 0 }}
        animate={{ y: 0, opacity: 1 }}
        transition={{ delay: 0.3 }}
        className={cn(
          'text-lg font-semibold mb-2',
          isDark ? 'text-neutral-100' : 'text-text-primary',
        )}
      >
        {title}
      </motion.h3>

      <motion.p
        initial={{ y: 10, opacity: 0 }}
        animate={{ y: 0, opacity: 1 }}
        transition={{ delay: 0.4 }}
        className="text-sm text-neutral-500 max-w-xs mb-6"
      >
        {desc}
      </motion.p>

      {actionSlot ? (
        <motion.div
          initial={{ y: 10, opacity: 0 }}
          animate={{ y: 0, opacity: 1 }}
          transition={{ delay: 0.5 }}
        >
          {actionSlot}
        </motion.div>
      ) : (
        onAction &&
        (actionLabel || d.action) && (
          <motion.button
            initial={{ y: 10, opacity: 0 }}
            animate={{ y: 0, opacity: 1 }}
            transition={{ delay: 0.5 }}
            onClick={onAction}
            className={cn(
              'px-5 py-3 rounded-xl text-sm font-semibold transition-[background-color,border-color,transform] duration-300',
              isError
                ? 'bg-red-500/15 text-red-300 border border-red-500/20 hover:bg-red-500/25'
                : cn(
                    'border',
                    isDark
                      ? 'bg-white/[0.07] text-white border-white/10 hover:bg-white/[0.12] hover:border-white/20'
                      : 'bg-black/[0.06] text-text-primary border-black/[0.08] hover:bg-black/[0.1] hover:border-black/[0.15]',
                  ),
            )}
          >
            {actionLabel || d.action}
          </motion.button>
        )
      )}
    </div>
  )
}
